# Numérico → permite promedio
edades = [25, 30, 35]
print("Promedio de edades:", sum(edades)/len(edades))

# Categórico → permite conteos
colores = ["rojo", "azul", "rojo"]
print("Conteo de 'rojo':", colores.count("rojo"))

# Temporal → permite ordenar
fechas = ["2025-01-03", "2025-01-01", "2025-01-02"]
print("Fechas ordenadas:", sorted(fechas))