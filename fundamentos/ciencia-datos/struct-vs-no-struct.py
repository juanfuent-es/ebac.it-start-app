# Ejemplo de datos estructurados (tabla)
datos_estructurados = [
    {"id": 1, "nombre": "Ana", "edad": 28},
    {"id": 2, "nombre": "Juan", "edad": 36}
]
print("Datos estructurados:")
for fila in datos_estructurados:
    print(fila)

# Ejemplo de datos no estructurados (texto libre)
texto = "Hoy Ana fue al gimnasio y después Juan tomó un café."
print("\nDato no estructurado:")
print(texto)